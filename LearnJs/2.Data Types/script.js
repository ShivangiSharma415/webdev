// Number Data Type
let a = 23;

console.log("The value of a is:", a);
console.log("Type of a is:" , typeof(a));

// String Data Type
let name = "Shivangi"

console.log("The value of name is:", name);
console.log("Type of name is:" , typeof(name));


// Boolean Data Type
let x = true

console.log("Type of x is:", x);


